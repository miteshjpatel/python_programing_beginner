# Python Lists and Tuplies

fruits = ['apple', 'banana', 'grape']

print(fruits)
print(fruits[1])

fruits.append('strawberry')

print(fruits)

fruits[1] = 'blueberry'

print(fruits)

tuple_name = ('Mitesh', 'Yogita', 'Shivam', 'Dev')
list_name = ['Mitesh', 'Yogita', 'Shivam', 'Dev']

print(list_name)
print(tuple_name)

list_name[0] = 'Mitesh Patel'

print(list_name)
print(list_name[0])

print(tuple_name.count('Mitesh'))
print(tuple_name[0])